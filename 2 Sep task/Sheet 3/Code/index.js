function classify(arr){
    let Positive=0, Negative=0, Even=0, Odd=0
    for (let i = 0; i < arr.length; i++) {
        if(arr[i]>0)
            Positive++
        else if (arr[i]<0)
            Negative++
        if(arr[i]%2==0)
            Even++
        else
            Odd++
    }
    console.log("Positive: "+Positive);
    console.log("Negative: "+Negative);
    console.log("Even: "+Even);
    console.log("Odd: "+Odd);
}

